#include<iostream>
using namespace std;
int main()
{
    int no1,no2;

    cout<<"Enter NO1 : ";
    cin>>no1;

    cout<<"Enter NO2 : ";
    cin>>no2;

    cout<<endl;

    cout<<"Addition : "<<no1<<" + "<<no2<<" = "<<no1+no2<<endl;
    cout<<"Subtraction : "<<no1<<" - "<<no2<<" = "<<no1-no2<<endl;
    cout<<"Multiplication : "<<no1<<" * "<<no2<<" = "<<no1*no2<<endl;
    cout<<"Division : "<<no1<<" / "<<no2<<" = "<<no1/no2<<endl;

    return 0;
}
