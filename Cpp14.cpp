#include<iostream>
#include<cmath> //neccessary to include cmath
using namespace std;
int main()
{
    cout<<"Minimum is : "<<min(4,8)<<endl;
    cout<<"Maximum is : "<<max(2,10)<<endl;
    cout<<"Square root is : "<<sqrt(64)<<endl;
    cout<<"Minimum is : "<<min(5.625,6.111)<<endl;
    cout<<"Maximum is : "<<max(6.421,6.321)<<endl;
    cout<<"2 to  the power of 3 is : "<<pow(2,3)<<endl;
    cout<<"Round value is : "<<round(5.3)<<endl;
    cout<<"Round value is : "<<round(5.7)<<endl;

    return 0;
}
