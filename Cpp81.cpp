//One dimensional array Method 3
#include<iostream>
using namespace std;
int main()
{
    int a[5]; //declaration
    a[0]=6;
    a[1]=10;
    a[2]=15;
    a[3]=-5;
    a[4]=3;

    cout<<"a[0] : "<<a[0]<<endl;
    cout<<"a[1] : "<<a[1]<<endl;
    cout<<"a[2] : "<<a[2]<<endl;
    cout<<"a[3] : "<<a[3]<<endl;
    cout<<"a[4] : "<<a[4]<<endl;

    return 0;
}
